/**
 * @see SILEC.h
 * @private
 * Write the register of UCN5895 truth a PCF8574.
 * @param silec the silec
 * @param data data of 28 MSB bits to transfert
 */
void _writeUCN5895(Silec* silec,int row,int *ramAddress) {
    //SilecData* silecData = &(silec->silecData);
    //Varialbe initialisation
    int i;   
    int j;
    
    int data = 0 ; //   ENABLE->D2=0   CLK->D1=0   DATA->D0=0
    int bitToTransfert = 0;
    int dataLine;
    data = data + row;
           
    //CLK = 0 STROBE = 0  DATA = 0
    STROBE_UCN5895 = 0;
    SER_IN_UCN5895 = 0;
    CLOCK_UCN5895 = 0;
    
    //read the 16 characters of the display
    for (j = 0; j<16; j++){ 
        
        dataLine = *ramAddress ;

        //1rst bit to  transfert
        bitToTransfert = (dataLine & 0x01) ;    
        dataLine = dataLine >> 1;
        if (bitToTransfert == 0){ 
            SER_IN_UCN5895 = 0;      
        }
        else {
            SER_IN_UCN5895 = 1;     
        }

        //Clock up
 //      CLOCK_UCN5895 = 1;   

        //read one row of character
        for (i = 0; i<6; i++){

        //Clock up
            CLOCK_UCN5895 = 1;  

            bitToTransfert = (dataLine & 0x01) ;    
            dataLine = dataLine >> 1;

        //bit to  transfert
            if (bitToTransfert == 0){ 
                SER_IN_UCN5895 = 0;   //bit=0
            }
            else {
                SER_IN_UCN5895 = 1;   //bit=1
            }

        //Clock down
            CLOCK_UCN5895 = 0;   
        }  
        ramAddress = ramAddress + 7;
    }
    
    //the row is latched, ready for print   
    delay100us(10);// to fix a quality default of print
 
    //deactivate of the row
    E1_HC237 = 1;
    //strobe up
    STROBE_UCN5895 = 1;
    
    //select the row to print
    A0_HC237 = row & 0x01;
    A1_HC237 = (row & 0x02)>>1;
    A2_HC237 = (row & 0x04)>>2;
    
    //print the row
    E1_HC237 = 0;
}

